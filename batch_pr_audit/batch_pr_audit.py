def main() -> int:
    args = parse_args()
    prs = load_pr_numbers(args)
    if not prs:
        print("No PRs provided. Use --prs, --prs-file, or --query.", file=sys.stderr)
        return 2
    if not args.skill_dir.exists():
        print(f"Skill dir not found: {args.skill_dir}", file=sys.stderr)
        return 2
    if not args.prompt_template.exists():
        print(f"Prompt template not found: {args.prompt_template}", file=sys.stderr)
        return 2

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    run_dir = args.report_dir / timestamp
    run_dir.mkdir(parents=True, exist_ok=True)
    prompt_template = args.prompt_template.read_text()

    print("=" * 60)
    print("Batch PR Audit")
    print(f"Run dir: {run_dir}")
    print(f"PRs: {', '.join(map(str, prs))}")
    print(f"Workers: {args.max_workers}")
    print(f"Agent: {'disabled' if args.no_agent else args.model or args.claude_bin}")
    print("=" * 60)

    results: list[AuditResult] = []
    status_lock = threading.Lock()
    status_file = run_dir / "status.json"

    def save_status() -> None:
        with status_lock:
            status_file.write_text(json.dumps([asdict(item) for item in results], indent=2, ensure_ascii=False) + "\n")

    with ThreadPoolExecutor(max_workers=max(1, args.max_workers)) as executor:
        futures = {
            executor.submit(audit_one_pr, pr, args, run_dir, prompt_template): pr
            for pr in prs
        }
        for future in as_completed(futures):
            pr = futures[future]
            result = future.result()
            results.append(result)
            save_status()
            print(
                f"[{result.verdict}] PR #{pr} severity={result.highest_severity} "
                f"blocking={result.blocking_count} report={result.report_file}"
            )

    write_summary(run_dir, results)
    print(f"Summary: {run_dir / 'summary.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
