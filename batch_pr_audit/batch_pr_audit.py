def check_added_lines(added_by_file: dict[str, list[tuple[int | None, str]]]) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    private_api: list[str] = []
    rtol_hits: list[str] = []
    bad_test_names: list[str] = []
    skip_without_issue: list[str] = []
    print_hits: list[str] = []
    test_print_hits: list[str] = []
    benchmark_print_hits: list[str] = []
    yaml_long_lines: list[str] = []
    py_long_lines: list[str] = []
    trailing_whitespace: list[str] = []
    benchmark_fstring_op_name: list[str] = []
    coauthored: list[str] = []
    hardcoded_test_dtype: list[str] = []
    hardcoded_bench_dtype: list[str] = []
    direct_cpu_reference: list[str] = []
    nan_without_equal_nan: list[str] = []

    for path, lines in added_by_file.items():
        file_text = "\n".join(text for _, text in lines)
        file_has_equal_nan = "equal_nan=True" in file_text
        for idx, (line_no, text) in enumerate(lines):
            stripped = text.strip()
            previous_added = lines[idx - 1][1].strip() if idx > 0 else ""
            if path.startswith(("tests/", "benchmark/")) and re.search(r"\btorch\._[A-Za-z]", stripped):
                private_api.append(evidence_line(path, line_no, text))
            if "gems_assert_close" in stripped and re.search(r"\brtol\s*=", stripped):
                rtol_hits.append(evidence_line(path, line_no, text))
            if path.startswith(("tests/", "benchmark/")) and re.search(r"\bdef\s+test_(?:perf_|accuracy_)", stripped):
                bad_test_names.append(evidence_line(path, line_no, text))
            if "pytest.mark.skip" in stripped or "pytest.mark.skipif" in stripped or "pytest.mark.xfail" in stripped:
                if not re.search(r"(https?://|#[0-9]+)", stripped):
                    skip_without_issue.append(evidence_line(path, line_no, text))
            if path.startswith("src/flag_gems/ops/") and re.search(r"\bprint\s*\(", stripped):
                print_hits.append(evidence_line(path, line_no, text))
            if path.startswith("tests/") and re.search(r"\bprint\s*\(", stripped):
                test_print_hits.append(evidence_line(path, line_no, text))
            if path.startswith("benchmark/") and re.search(r"\bprint\s*\(", stripped):
                benchmark_print_hits.append(evidence_line(path, line_no, text))
            if path == "conf/operators.yaml" and len(text) > 120:
                yaml_long_lines.append(evidence_line(path, line_no, f"len={len(text)} {text}"))
            if path.endswith(".py") and len(text) > 120:
                py_long_lines.append(evidence_line(path, line_no, f"len={len(text)} {text}"))
            if text.rstrip() != text:
                trailing_whitespace.append(evidence_line(path, line_no, text))
            if path.startswith("benchmark/") and re.search(r"\bop_name\s*=\s*f['\"]", stripped):
                benchmark_fstring_op_name.append(evidence_line(path, line_no, text))
            if "Co-Authored-By:" in stripped:
                coauthored.append(evidence_line(path, line_no, text))
            dtype_pattern = r"\[torch\.(?:float32|float64|float16|bfloat16)(?:\s*,\s*torch\.(?:float32|float64|float16|bfloat16))*\]"
            if path.startswith("tests/") and re.search(dtype_pattern, stripped) and not previous_added.startswith("#"):
                hardcoded_test_dtype.append(evidence_line(path, line_no, text))
            if path.startswith("benchmark/") and re.search(dtype_pattern, stripped) and not previous_added.startswith("#"):
                hardcoded_bench_dtype.append(evidence_line(path, line_no, text))
            if path.startswith("tests/") and "to_reference" not in stripped:
                if ".cpu()" in stripped or ".to(torch.float64)" in stripped:
                    direct_cpu_reference.append(evidence_line(path, line_no, text))
            if path.startswith("tests/") and re.search(r"\bnan\b|NaN|torch\.nan", stripped) and not file_has_equal_nan:
                nan_without_equal_nan.append(evidence_line(path, line_no, text))

    if private_api:
        add("high", "private_torch_api", "Tests/benchmarks should not call torch private APIs.", private_api)
    if rtol_hits:
        add("high", "gems_assert_close_rtol", "gems_assert_close should not pass rtol.", rtol_hits)
    if bad_test_names:
        add("medium", "bad_test_function_name", "Test functions should be named test_<op>, not test_perf_ or test_accuracy_.", bad_test_names)
    if skip_without_issue:
        add("high", "skip_without_issue", "New skip/xfail marks must cite an issue URL or #number.", skip_without_issue)
    if print_hits:
        add("medium", "print_in_operator", "Operator implementation should not use print().", print_hits)
    if test_print_hits:
        add("high", "print_in_test", "Tests should not use print().", test_print_hits)
    if benchmark_print_hits:
        add("low", "print_in_benchmark", "Benchmark should avoid print().", benchmark_print_hits)
    if yaml_long_lines:
        add("low", "yaml_long_line", "operators.yaml added lines should avoid long single-line text.", yaml_long_lines)
    if py_long_lines:
        add("low", "python_long_line", "Added Python lines over 120 characters may fail style checks.", py_long_lines)
    if trailing_whitespace:
        add("medium", "trailing_whitespace", "Added lines contain trailing whitespace.", trailing_whitespace)
    if benchmark_fstring_op_name:
        add("medium", "benchmark_dynamic_op_name", "benchmark op_name should be a fixed canonical/variant id, not an f-string.", benchmark_fstring_op_name)
    if coauthored:
        add("high", "co_authored_by", "Commit/PR text must not include Co-Authored-By.", coauthored)
    if hardcoded_test_dtype:
        add("high", "test_dtype_hardcoded", "Test dtype lists should use utils.FLOAT_DTYPES or have a comment explaining hardcoding.", hardcoded_test_dtype)
    if hardcoded_bench_dtype:
        add("high", "benchmark_dtype_hardcoded", "Benchmark dtype lists should use consts.FLOAT_DTYPES or have a comment explaining hardcoding.", hardcoded_bench_dtype)
    if direct_cpu_reference:
        add("low", "manual_cpu_reference", "Tests should prefer utils.to_reference instead of direct .cpu()/.to(torch.float64).", direct_cpu_reference)
    if nan_without_equal_nan:
        add("low", "nan_without_equal_nan", "NaN-related tests should use gems_assert_close(equal_nan=True).", nan_without_equal_nan)

    return findings


def check_new_file_structure(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    for path in added_by_file:
        if not is_added_new_file(path, added_by_file):
            continue
        text = added_text_for(path, added_by_file)
        if re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            if not text.startswith(REQUIRED_KERNEL_HEADER):
                add("high", "kernel_header", "New operator file must start with the fixed copyright/Apache License and KernelGen header.", [path])
            if "import logging" not in text or "logger" not in text:
                add("low", "missing_logging", "New operator file should use the logging module.", [path])

        if re.match(r"tests/test_.+\.py$", path):
            evidence: list[str] = []
            if "from . import accuracy_utils as utils" not in text:
                evidence.append("missing: from . import accuracy_utils as utils")
            if "flag_gems.use_gems()" not in text:
                evidence.append("missing: flag_gems.use_gems()")
            if "gems_assert_close" not in text and "gems_assert_equal" not in text:
                evidence.append("missing: utils.gems_assert_close or utils.gems_assert_equal")
            if "utils.to_reference" not in text:
                evidence.append("missing: utils.to_reference")
            if evidence:
                add("high", "test_file_structure", "New test file is missing required FlagGems test patterns.", [f"{path}: {item}" for item in evidence])

        if re.match(r"benchmark/test_.+\.py$", path):
            evidence = []
            if "from . import base" not in text:
                evidence.append("missing: from . import base")
            if "FLOAT_DTYPES" not in text and not re.search(r"\[torch\.(?:float32|float64|float16|bfloat16)", text):
                evidence.append("missing dtype coverage: consts.FLOAT_DTYPES or commented hardcoded dtype")
            benchmark_classes = (
                "UnaryPointwiseBenchmark",
                "BinaryPointwiseBenchmark",
                "ReductionBenchmark",
                "UnaryPointwiseIsCloseBenchmark",
                "GenericBenchmark",
                "BlasBenchmark",
                "OperationBenchmark",
            )
            if not any(cls in text for cls in benchmark_classes) and ".run()" not in text:
                evidence.append("missing benchmark wrapper class/run() usage")
            if evidence:
                add("medium", "benchmark_file_structure", "New benchmark file is missing expected benchmark patterns.", [f"{path}: {item}" for item in evidence])
            nested_classes = added_line_evidence(path, added_by_file, r"^\s+class\s+[A-Za-z_][A-Za-z0-9_]*Benchmark\b")
            if nested_classes:
                add("medium", "benchmark_class_nested", "Custom benchmark classes should be defined at module scope, not inside test functions.", nested_classes)
            empty_override = re.search(
                r"def\s+(set(?:_more)?_shapes)\s*\([^)]*\):\s*\n\s+super\(\)\.\1\s*\([^)]*\)",
                text,
                re.MULTILINE,
            )
            if empty_override:
                add("low", "benchmark_empty_override", "Do not keep empty set_shapes/set_more_shapes overrides that only call super().", [f"{path}: {empty_override.group(1)}"])

    probability_ids = [item for item in current_ids if any(hint in item for hint in PROBABILITY_OP_HINTS)]
    if probability_ids:
        test_text = "\n".join(
            added_text_for(path, added_by_file)
            for path in added_by_file
            if path.startswith("tests/")
        )
        has_stats = re.search(r"\b(mean|var|variance|std)\s*\(", test_text) or re.search(r"\bmean\b|\bvariance\b|\bstd\b", test_text)
        if "gems_assert_close" in test_text and not has_stats:
            add(
                "medium",
                "probability_test_not_statistical",
                "Probability/random operators should use statistical validation rather than only exact/close comparison.",
                probability_ids,
            )

    return findings


def check_backend_specialization_files(
    *,
    files: list[str],
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
    backend_state: dict[str, set[str]],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    if not backend_state:
        return findings

    for backend, ids in sorted(backend_state.items()):
        init_path = f"src/flag_gems/runtime/backend/{backend}/ops/__init__.py"
        tune_path = f"src/flag_gems/runtime/backend/{backend}/tune_configs.yaml"
        has_init = init_path in files
        has_tune = tune_path in files
        init_text = added_text_for(init_path, added_by_file)

        if not has_init:
            add(
                "high",
                "backend_missing_ops_init",
                "Backend-specialized PR should update backend ops/__init__.py for every touched backend.",
                [init_path],
            )
        else:
            missing_exports: list[str] = []
            for op_name in sorted(ids):
                simple_name = canonical_variant_id(op_name)
                if simple_name not in init_text:
                    missing_exports.append(f"{init_path}: missing import/export hint for {simple_name}")
            if missing_exports:
                add(
                    "medium",
                    "backend_ops_init_incomplete",
                    "Backend ops/__init__.py does not appear to import/export every touched backend operator.",
                    missing_exports,
                )

        if has_tune:
            tune_text = added_text_for(tune_path, added_by_file)
            unrelated = []
            for _, text in added_by_file.get(tune_path, []):
                stripped = text.strip()
                if not stripped or stripped.startswith("#"):
                    continue
                if stripped.startswith("-") and "operator" not in stripped:
                    continue
                if any(belongs_to_current(token, current_ids) for token in re.findall(r"[A-Za-z_][A-Za-z0-9_]*", stripped)):
                    continue
                if any(op in stripped for op in ids):
                    continue
                unrelated.append(stripped[:220])
            if unrelated and not tune_text:
                add(
                    "medium",
                    "backend_tune_config_unclear",
                    "Backend tune_configs.yaml changed, but added lines do not clearly map to the current operator.",
                    [f"{tune_path}: {line}" for line in unrelated[:6]],
                )

    backend_only_paths = [
        path for path in files
        if path.startswith("src/flag_gems/runtime/backend/")
        and not (
            BACKEND_OP_PATH_RE.match(path)
            or BACKEND_OPS_INIT_RE.match(path)
            or BACKEND_TUNE_CONFIG_RE.match(path)
        )
    ]
    if backend_only_paths:
        add(
            "medium",
            "backend_unexpected_path",
            "Backend-specialized PR touches backend paths outside the minimal operator-scoped allowlist.",
            backend_only_paths,
        )

    return findings


