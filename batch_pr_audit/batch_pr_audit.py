def check_fused_layout(
    *,
    files: list[str],
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
    fused_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    fused_ops_in_ops_dir = []
    for path in files:
        match = re.match(r"src/flag_gems/ops/(?!__init__\.py)(.+)\.py$", path)
        if not match:
            continue
        op_name = canonical_variant_id(match.group(1))
        if op_name in fused_ids or "fused" in op_name:
            fused_ops_in_ops_dir.append(path)
    if fused_ops_in_ops_dir:
        add(
            "high",
            "fused_op_in_ops_dir",
            "Fused operators should live under src/flag_gems/fused/, not src/flag_gems/ops/.",
            fused_ops_in_ops_dir,
        )

    generic_fused_files = [path for path in files if FUSED_OP_PATH_RE.match(path)]
    if generic_fused_files:
        init_text = added_text_for("src/flag_gems/fused/__init__.py", added_by_file)
        if "src/flag_gems/fused/__init__.py" not in files:
            add(
                "high",
                "fused_init_missing",
                "PR touching generic fused operators should update src/flag_gems/fused/__init__.py.",
                ["src/flag_gems/fused/__init__.py"],
            )
        else:
            missing = []
            for path in generic_fused_files:
                op_name = canonical_variant_id(Path(path).stem)
                if op_name not in init_text:
                    missing.append(f"src/flag_gems/fused/__init__.py: missing import/export hint for {op_name}")
            if missing:
                add(
                    "medium",
                    "fused_init_incomplete",
                    "src/flag_gems/fused/__init__.py does not appear to import/export every touched fused operator.",
                    missing,
                )

    backend_non_fused = [
        path for path in files
        if BACKEND_FUSED_OP_PATH_RE.match(path)
        and canonical_variant_id(BACKEND_FUSED_OP_PATH_RE.match(path).group(2)) not in fused_ids
    ]
    if backend_non_fused:
        add(
            "medium",
            "backend_fused_path_mismatch",
            "Backend fused/ path changed, but the operator does not look like a fused operator.",
            backend_non_fused,
        )

    return findings


def check_benchmark_fairness(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
    fused_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    current_norm = {canonical_variant_id(item) for item in current_ids} | fused_ids
    suspect_hits: list[str] = []
    for path, lines in added_by_file.items():
        if not path.startswith("benchmark/") or not path.endswith(".py"):
            continue
        text = "\n".join(line for _, line in lines)
        if "gems_op=" not in text:
            continue
        if not any(op_id in canonical_variant_id(Path(path).stem) or op_id in text for op_id in current_norm):
            continue
        if re.search(r"torch_op\s*=\s*torch\.[A-Za-z_][A-Za-z0-9_]*", text) and "gems_op=" in text:
            continue
        if "torch.ops.aten." in text and "gems_op=" in text:
            continue
        wrapper_defs = re.findall(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", text)
        wrapper_defs = [name for name in wrapper_defs if "gems" not in name.lower() and ("torch" in name.lower() or "ref" in name.lower() or "wrapper" in name.lower())]
        if wrapper_defs and ("torch.nn.functional" in text or "torch.autograd" in text or ".backward(" in text):
            suspect_hits.append(f"{path}: custom torch wrapper may measure extra work compared with gems_op")
    if suspect_hits:
        add(
            "medium",
            "benchmark_fairness_suspect",
            "Benchmark torch path may be doing extra work beyond the gems path; verify equivalent measurement scope.",
            suspect_hits,
        )

    return findings


def check_specialization_pr_dependency(
    *,
    metadata: dict,
    files: list[str],
    current_ids: set[str],
    backend_state: dict[str, set[str]],
    repo: str,
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []
    if not backend_state:
        return findings

    title = metadata.get("title") or ""
    head_ref = metadata.get("headRefName") or ""
    generic_frontend_files = [
        path for path in files
        if re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path)
    ]
    if generic_frontend_files:
        return findings
    if not re.search(r"特化|speciali[sz]ed|backend", title, re.IGNORECASE) and not re.search(
        r"(backend|speciali[sz]ed|特化)",
        head_ref,
        re.IGNORECASE,
    ):
        return findings

    evidence: list[str] = []
    for op_id in sorted({base_id(item) for item in current_ids}):
        query = f"is:pr is:open repo:{repo} in:title \"Add {op_id} operator\""
        result = run_cmd(
            ["gh", "pr", "list", "--repo", repo, "--search", query, "--json", "number,title,state,mergedAt", "--limit", "5"],
            Path.cwd(),
            timeout=60,
        )
        if result.returncode != 0:
            continue
        try:
            items = json.loads(result.stdout)
        except json.JSONDecodeError:
            continue
        visible = []
        for item in items:
            if not isinstance(item, dict):
                continue
            item_title = item.get("title") or ""
            if item_title == title:
                continue
            if base_id(canonical_variant_id(op_id)) not in canonical_variant_id(item_title):
                visible.append(f"candidate PR #{item.get('number')}: {item_title}")
            else:
                visible.append(f"candidate PR #{item.get('number')}: {item_title}")
        if not visible:
            evidence.append(
                f"{op_id}: no obvious generic/open PR found for backend-specialized branch {head_ref or '(unknown branch)'}"
            )
    if evidence:
        findings.append((
            "medium",
            "specialization_without_obvious_generic_pr",
            "Backend-specialized PR should usually follow an existing generic implementation or generic PR.",
            evidence[:12],
        ))
    return findings


def extract_yaml_added_blocks(added_by_file: dict[str, list[tuple[int | None, str]]]) -> dict[str, list[str]]:
    blocks: dict[str, list[str]] = {}
    current_id: str | None = None
    current_lines: list[str] = []
    for _, text in added_by_file.get("conf/operators.yaml", []):
        match = re.match(r"\s*-\s+id:\s+(\S+)", text)
        if match:
            if current_id:
                blocks[current_id] = current_lines
            current_id = match.group(1).strip()
            current_lines = [text]
        elif current_id:
            current_lines.append(text)
    if current_id:
        blocks[current_id] = current_lines
    return blocks


def check_yaml_added_blocks(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []
    blocks = extract_yaml_added_blocks(added_by_file)

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    for op_id, lines in blocks.items():
        if current_ids and not belongs_to_current(op_id, current_ids):
            continue
        text = "\n".join(lines)
        missing = [
            field for field in ("id:", "description:", "for:", "labels:", "kind:", "stages:")
            if field not in text
        ]
        if missing:
            add("high", "yaml_missing_fields", "New operators.yaml block is missing required fields.", [f"{op_id}: {', '.join(missing)}"])
        if "KernelGen" not in text:
            add("medium", "yaml_missing_kernelgen_label", "New operators.yaml labels should include KernelGen.", [op_id])
        if not re.search(r"^\s*-\s+aten\s*$", text, re.MULTILINE):
            add("medium", "yaml_missing_aten_label", "New operators.yaml labels should include aten.", [op_id])
        if re.search(r"Triton kernel implementation for|Computes the .* operation", text):
            add("low", "yaml_placeholder_description", "operators.yaml description should not be a template placeholder.", [op_id])
        if re.search(r"\bstable\b|alpha:\s*['\"]?(?:1\.0|2\.0|3\.0|4\.)", text):
            add("medium", "yaml_stage_too_strong", "New KernelGen operator metadata should use conservative alpha/incubating stages.", [op_id])
        kind_hits = re.findall(r"^\s*-\s+([A-Za-z]+)\s*$", text, re.MULTILINE)
        kinds = [item for item in kind_hits if item in VALID_YAML_KINDS or item in {"Unknown", "UNKNOWN"}]
        for kind in kinds:
            if kind not in VALID_YAML_KINDS:
                add("low", "yaml_unknown_kind", "operators.yaml kind is not in the known kind list.", [f"{op_id}: {kind}"])
        if base_id(op_id) in TENSOR_KIND_OPS and re.search(r"^\s*-\s+Math\s*$", text, re.MULTILINE):
            add("medium", "yaml_kind_tensor_as_math", "Tensor-like operators should not use kind: Math.", [op_id])

    duplicate_added = sorted({item for item in blocks if list(blocks).count(item) > 1})
    if duplicate_added:
        add("high", "yaml_duplicate_added_id", "PR adds duplicate operators.yaml ids.", duplicate_added)

    return findings


def extract_kernel_function_sets(
    added_by_file: dict[str, list[tuple[int | None, str]]],
) -> tuple[set[str], set[str]]:
    jit_funcs: set[str] = set()
    all_funcs: set[str] = set()
    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        decorators: list[str] = []
        for _, text in lines:
            stripped = text.strip()
            if stripped.startswith("@"):
                decorators.append(stripped)
                continue
            match = re.match(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", text)
            if match:
                func_name = match.group(1)
                all_funcs.add(func_name)
                if any("triton.jit" in deco or deco.endswith(".jit") or deco == "@jit" for deco in decorators):
                    jit_funcs.add(func_name)
                decorators = []
            elif stripped and not stripped.startswith("#"):
                decorators = []
    return all_funcs, jit_funcs


def check_registration_surfaces(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    added_public_functions: set[str],
    added_full_config: list[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []
    _, jit_funcs = extract_kernel_function_sets(added_by_file)
    ops_init_text = added_text_for("src/flag_gems/ops/__init__.py", added_by_file)
    top_init_text = added_text_for("src/flag_gems/__init__.py", added_by_file)

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    if added_public_functions and ops_init_text:
        for func_name in sorted(added_public_functions):
            if func_name not in ops_init_text:
                add("high", "ops_init_missing_export", "ops/__init__.py should import and export every new public wrapper.", [func_name])
    if jit_funcs and ops_init_text:
        leaked = [name for name in sorted(jit_funcs) if re.search(rf"\b{name}\b", ops_init_text)]
        if leaked:
            add("high", "ops_init_exports_kernel", "ops/__init__.py must not export Triton kernel functions.", leaked)

    config_keys = [entry.split(" -> ", 1)[0] for entry in added_full_config]
    duplicates = sorted({key for key in config_keys if config_keys.count(key) > 1})
    if duplicates:
        add("high", "full_config_duplicate_added", "_FULL_CONFIG should not add duplicate aten names.", duplicates)

    if added_full_config and jit_funcs:
        bad_targets = []
        for entry in added_full_config:
            _, target = entry.split(" -> ", 1)
            if target in jit_funcs:
                bad_targets.append(entry)
        if bad_targets:
            add("high", "full_config_maps_to_kernel", "_FULL_CONFIG must map aten names to public wrappers, not Triton kernels.", bad_targets)

    if added_public_functions and top_init_text:
        for func_name in sorted(added_public_functions):
            target_patterns = [func_name]
            if not func_name.startswith("_"):
                target_patterns.append(f"_{func_name}")
            if not any(
                re.search(rf'\(\s*["\'][^"\']+["\']\s*,\s*{re.escape(target)}\b', top_init_text)
                for target in target_patterns
            ):
                add("high", "full_config_missing_wrapper", "_FULL_CONFIG should register every new public wrapper.", [func_name])

    return findings


def check_kernel_risks(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        text = "\n".join(line for _, line in lines)
        duplicate_names: dict[str, int] = {}
        for _, line in lines:
            match = re.match(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", line)
            if match:
                duplicate_names[match.group(1)] = duplicate_names.get(match.group(1), 0) + 1
        duplicates = sorted(name for name, count in duplicate_names.items() if count > 1)
        if duplicates:
            add("high", "duplicate_function_defs", "Added operator file contains duplicate function definitions.", [f"{path}: {name}" for name in duplicates])

        recursive_hits: list[str] = []
        variants = {item for current in current_ids for item in {canonical_variant_id(current), base_id(current)}}
        for line_no, line in lines:
            code_part = line.split("#", 1)[0]
            if "torch.ops." in code_part:
                continue
            for variant in variants:
                if re.search(rf"\btorch\.{re.escape(variant)}\s*\(", code_part):
                    recursive_hits.append(evidence_line(path, line_no, line))
        if recursive_hits:
            add("high", "fallback_recursion", "Operator wrapper should not call torch.<same_op>() because it can recurse through dispatch.", recursive_hits)

        alias_hits = [
            evidence_line(path, line_no, line)
            for line_no, line in lines
            if re.match(r"\s*[A-Za-z_][A-Za-z0-9_]*\s*=\s*tl_extra_shim\.[A-Za-z_][A-Za-z0-9_]*\s*$", line)
        ]
        if alias_hits:
            add("low", "alias_wrapper", "Avoid meaningless aliases for tl_extra_shim functions; call the original function directly.", alias_hits)

        global_hits = []
        for line_no, line in lines:
            stripped = line.strip()
            if not stripped or line.startswith((" ", "\t")):
                continue
            if not re.match(r"[A-Za-z_][A-Za-z0-9_]*\s*=", stripped):
                continue
            name = stripped.split("=", 1)[0].strip()
            if name.isupper() or name in {"logger"}:
                continue
            if stripped.startswith(("__all__", "_FULL_CONFIG")):
                continue
            if "tl_extra_shim." in stripped:
                continue
            global_hits.append(evidence_line(path, line_no, line))
        if global_hits:
            add("low", "kernel_module_global", "Reviewer guidance discourages module-level mutable/global variables in operator files.", global_hits)

        inline_configs = added_line_evidence(path, added_by_file, r"triton\.Config\s*\(")
        if inline_configs:
            add("medium", "inline_autotune_config", "Prefer external tune_configs.yaml/get_tuned_config over inline triton.Config blocks.", inline_configs)
        if "@triton.autotune" in text and "get_tuned_config" not in text:
            add("low", "autotune_without_get_tuned_config", "triton.autotune should usually use runtime.get_tuned_config or documented precedent.", [path])

        use_tl_extra_stubs: list[str] = []
        for idx, (line_no, line) in enumerate(lines):
            if "@use_tl_extra" not in line:
                continue
            following = "\n".join(item[1] for item in lines[idx + 1: idx + 6])
            match = re.search(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", following)
            if match and re.search(r"\bpass\b", following):
                func_name = match.group(1)
                if len(re.findall(rf"\b{re.escape(func_name)}\b", text)) <= 1:
                    use_tl_extra_stubs.append(f"{path}:{line_no}: {func_name}")
        if use_tl_extra_stubs:
            add("high", "unused_use_tl_extra_stub", "Unused @use_tl_extra pass stubs should be removed.", use_tl_extra_stubs)

    return findings


def check_logger_usage(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    added_public_functions: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append((severity, check, message, evidence[:12]))

    bad_format: list[str] = []
    wrong_vendor: list[str] = []
    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        for line_no, line in lines:
            match = re.search(r'logger\.debug\(\s*["\']([^"\']+)["\']', line)
            if not match:
                continue
            msg = match.group(1)
            if msg != msg.upper():
                bad_format.append(evidence_line(path, line_no, line))
            if not re.match(r"^GEMS(?:_[A-Z0-9]+)?\s+[A-Z0-9_]+$", msg):
                bad_format.append(evidence_line(path, line_no, line))
            if re.match(r"^[A-Z0-9]+_?GEMS\s+", msg) or re.match(r"^[A-Z0-9]+\s+GEMS\s+", msg):
                wrong_vendor.append(evidence_line(path, line_no, line))
    if bad_format:
        add("low", "logger_format", "logger.debug message should be uppercase and formatted as GEMS <OP> or GEMS_VENDOR <OP>.", sorted(set(bad_format)))
    if wrong_vendor:
        add("medium", "logger_vendor_format", "Backend logger format should be GEMS_VENDOR <OP>, not VENDOR GEMS <OP>.", sorted(set(wrong_vendor)))

    position_hits: list[str] = []
    public_names = {canonical_variant_id(item) for item in added_public_functions}
    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        for idx, (line_no, line) in enumerate(lines):
            match = re.match(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", line)
            if not match or canonical_variant_id(match.group(1)) not in public_names:
                continue
            in_docstring = False
            for next_line_no, next_line in lines[idx + 1:]:
                if re.match(r"def\s+[A-Za-z_][A-Za-z0-9_]*\s*\(", next_line):
                    break
                stripped = next_line.strip()
                if not stripped or stripped.startswith("#"):
                    continue
                if stripped.startswith(('"""', "'''")):
                    if stripped.count('"""') == 1 or stripped.count("'''") == 1:
                        in_docstring = not in_docstring
                    continue
                if in_docstring:
                    continue
                if "logger.debug" not in stripped:
                    position_hits.append(evidence_line(path, next_line_no, f"{match.group(1)} first statement: {stripped}"))
                break
    if position_hits:
        add("low", "logger_not_first_statement", "Public wrapper logger.debug should be the first meaningful statement after the docstring.", position_hits)

    return findings


def extract_public_operator_functions(
    added_by_file: dict[str, list[tuple[int | None, str]]],
    current_ids: set[str],
) -> set[str]:
    functions: set[str] = set()
    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        op_file_stem = Path(path).stem
        for _, text in lines:
            match = re.match(r"def\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", text)
            if not match:
                continue
            func_name = match.group(1)
            if func_name.startswith("_") and not op_file_stem.startswith("_"):
                continue
            if "kernel" in func_name or func_name.endswith(("_meta", "_impl", "_fallback")):
                continue
            if current_ids and not belongs_to_current(func_name, current_ids):
                continue
            functions.add(canonical_variant_id(func_name))
    return functions


def extract_operator_variant_functions(
    added_by_file: dict[str, list[tuple[int | None, str]]],
) -> tuple[set[str], set[str]]:
    out_functions: set[str] = set()
    inplace_functions: set[str] = set()
    for path, lines in added_by_file.items():
        if not re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path):
            continue
        for _, text in lines:
            match = re.search(r"\bdef\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", text)
            if not match:
                continue
            func_name = match.group(1)
            if func_name.endswith("_out"):
                out_functions.add(func_name)
            elif func_name.endswith("_") and not func_name.startswith("__") and not func_name.endswith("__"):
                inplace_functions.add(func_name)
    return out_functions, inplace_functions


def check_public_function_coverage(
    *,
    added_public_functions: set[str],
    added_yaml_ids: list[str],
    added_pytest_marks: set[str],
    added_benchmark_op_names: set[str],
    current_ids: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []
    public_ids = {canonical_variant_id(item) for item in added_public_functions}
    yaml_ids = {canonical_variant_id(item) for item in added_yaml_ids}
    pytest_ids = {canonical_variant_id(item) for item in added_pytest_marks}
    benchmark_ids = {canonical_variant_id(item) for item in added_benchmark_op_names}

    scoped_yaml_ids = {item for item in yaml_ids if belongs_to_current(item, current_ids)}
    scoped_pytest_ids = {item for item in pytest_ids if belongs_to_current(item, current_ids)}
    scoped_benchmark_ids = {item for item in benchmark_ids if belongs_to_current(item, current_ids)}

    for func_id in sorted(public_ids):
        missing: list[str] = []
        if func_id not in scoped_yaml_ids:
            missing.append(f"missing yaml id: {func_id}")
        if func_id not in scoped_pytest_ids:
            missing.append(f"missing pytest mark: {func_id}")
        if func_id not in scoped_benchmark_ids:
            missing.append(f"missing benchmark op_name: {func_id}")
        if missing:
            findings.append((
                "high",
                "operator_function_missing_coverage",
                "New public operator function should have matching yaml id, pytest mark, and benchmark op_name.",
                [f"{func_id}: {item}" for item in missing],
            ))

    declared_ids = scoped_yaml_ids | scoped_pytest_ids | scoped_benchmark_ids
    for declared_id in sorted(declared_ids - public_ids):
        sources: list[str] = []
        if declared_id in scoped_yaml_ids:
            sources.append("yaml id")
        if declared_id in scoped_pytest_ids:
            sources.append("pytest mark")
        if declared_id in scoped_benchmark_ids:
            sources.append("benchmark op_name")
        findings.append((
            "high",
            "coverage_without_operator_function",
            "New yaml/test/benchmark operator entry should have a matching public operator function in the PR diff.",
            [f"{declared_id}: present in {', '.join(sources)}, missing public function def"],
        ))

    return findings


def check_variant_consistency(
    *,
    added_out_functions: set[str],
    added_inplace_functions: set[str],
    added_yaml_ids: list[str],
    added_full_config: list[str],
    added_pytest_marks: set[str],
    added_benchmark_op_names: set[str],
) -> list[tuple[str, str, str, list[str]]]:
    findings: list[tuple[str, str, str, list[str]]] = []
    config_keys = {entry.split(" -> ", 1)[0] for entry in added_full_config}

    def check_variant(func_name: str, variant_id: str, kind: str) -> None:
        evidence: list[str] = []
        if kind == "out" and not any(key.endswith(".out") and key.replace("special.", "special_").split(".out")[0] == variant_id[:-4] for key in config_keys):
            evidence.append(f"missing _FULL_CONFIG .out key for function {func_name}")
        if kind == "inplace" and not any(key.replace("special.", "special_").split(".")[0].lstrip("_") == variant_id for key in config_keys):
            evidence.append(f"missing _FULL_CONFIG key for inplace function {func_name}")
        if evidence:
            findings.append((
                "high",
                f"{kind}_variant_incomplete",
                f"Exported {kind} variant must have matching _FULL_CONFIG coverage.",
                evidence,
            ))

    for func_name in sorted(added_out_functions):
        check_variant(func_name, func_name, "out")
    for func_name in sorted(added_inplace_functions):
        check_variant(func_name, func_name.lstrip("_"), "inplace")
    return findings


def extract_speedup_findings(body: str, current_ids: set[str], threshold: float = 1.05) -> list[str]:
    findings: list[str] = []
    if not body:
        return findings
    for line in body.splitlines():
        if "|" not in line or "Arithmetic Mean" in line or "---" in line:
            continue
        cells = [cell.strip().strip("*") for cell in line.strip().strip("|").split("|")]
        if len(cells) < 2:
            continue
        op_name = cells[0].strip("`")
        if op_name not in current_ids:
            continue
        try:
            speedup = float(cells[1])
        except ValueError:
            continue
        if speedup < threshold:
            findings.append(f"{op_name}: arithmetic mean speedup {speedup:.3f} < {threshold:.2f}")
    return findings


