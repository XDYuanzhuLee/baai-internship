def run_streaming_agent(
    cmd: list[str],
    cwd: Path,
    timeout_seconds: int,
    env: dict[str, str],
    log_file: Path,
    conversation_file: Path,
    prompt: str,
) -> tuple[int, str, str]:
    stdout_chunks: list[str] = []
    stderr_chunks: list[str] = []
    deadline = time.monotonic() + timeout_seconds

    with log_file.open("w") as log, conversation_file.open("w") as stream:
        stream.write(json.dumps({
            "type": "meta",
            "time": utc_now(),
            "cwd": str(cwd),
            "command": cmd[:-1] + ["<prompt>"],
            "prompt": prompt,
        }, ensure_ascii=False) + "\n")
        stream.flush()

        proc = subprocess.Popen(
            cmd,
            cwd=str(cwd),
            env=env,
            text=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            bufsize=1,
        )
        sel = selectors.DefaultSelector()
        assert proc.stdout is not None
        assert proc.stderr is not None
        sel.register(proc.stdout, selectors.EVENT_READ, "stdout")
        sel.register(proc.stderr, selectors.EVENT_READ, "stderr")
        returncode = 1
        try:
            while sel.get_map():
                if time.monotonic() > deadline:
                    proc.kill()
                    raise subprocess.TimeoutExpired(cmd, timeout_seconds)
                for key, _ in sel.select(timeout=1.0):
                    line = key.fileobj.readline()
                    if line == "":
                        sel.unregister(key.fileobj)
                        continue
                    stream_name = key.data
                    if stream_name == "stdout":
                        stdout_chunks.append(line)
                    else:
                        stderr_chunks.append(line)
                    log.write(line)
                    log.flush()
                    record = {
                        "type": "stream",
                        "time": utc_now(),
                        "stream": stream_name,
                        "raw": line.rstrip("\n"),
                    }
                    try:
                        parsed = json.loads(line)
                    except json.JSONDecodeError:
                        parsed = None
                    if isinstance(parsed, dict):
                        record["event"] = parsed
                    stream.write(json.dumps(record, ensure_ascii=False) + "\n")
                    stream.flush()
            returncode = proc.wait(timeout=5)
            return returncode, "".join(stdout_chunks), "".join(stderr_chunks)
        finally:
            sel.close()
            stream.write(json.dumps({
                "type": "result",
                "time": utc_now(),
                "returncode": returncode,
            }, ensure_ascii=False) + "\n")


def extract_agent_report(stdout: str, stderr: str) -> str:
    texts: list[str] = []
    raw = stdout + "\n" + stderr
    for line in raw.splitlines():
        try:
            event = json.loads(line)
        except json.JSONDecodeError:
            continue
        if not isinstance(event, dict):
            continue
        if isinstance(event.get("result"), str):
            texts.append(event["result"])
        message = event.get("message")
        if isinstance(message, dict):
            for item in message.get("content", []):
                if isinstance(item, dict) and item.get("type") == "text":
                    text = item.get("text")
                    if isinstance(text, str):
                        texts.append(text)
    if texts:
        return texts[-1].strip()
    clean = ANSI_RE.sub("", raw).strip()
    return clean[-8000:] if clean else "AGENT_FAILED: no output"


def parse_machine_summary(report: str) -> dict:
    match = MACHINE_SUMMARY_RE.search(report)
    if not match:
        return {}
    try:
        return json.loads(match.group(1))
    except json.JSONDecodeError:
        return {}


def build_agent_cmd(args: argparse.Namespace, prompt: str, pr_dir: Path, checkout_dir: Path | None) -> list[str]:
    cmd = [
        args.claude_bin,
        "-p",
        "--verbose",
        "--output-format",
        "stream-json",
        "--permission-mode",
        args.permission_mode,
        "--add-dir",
        str(pr_dir),
        "--add-dir",
        str(args.skill_dir),
    ]
    if checkout_dir:
        cmd.extend(["--add-dir", str(checkout_dir)])
    if args.model:
        cmd.extend(["--model", args.model])
    if args.settings_file:
        cmd.extend(["--settings", str(args.settings_file)])
    if args.max_budget_usd is not None:
        cmd.extend(["--max-budget-usd", str(args.max_budget_usd)])
    cmd.append(prompt)
    return cmd


def audit_one_pr(pr: int, args: argparse.Namespace, run_dir: Path, prompt_template: str) -> AuditResult:
    start = utc_now()
    start_epoch = time.time()
    pr_dir = run_dir / f"pr_{pr}"
    report_file = pr_dir / "report.md"
    conversation_file = pr_dir / "conversation.jsonl"
    log_file = pr_dir / "raw.log"

    try:
        artifacts = collect_pr_artifacts(pr, args, pr_dir)
        checks = deterministic_checks(artifacts["metadata"], artifacts["files"], artifacts["diff"])
        (pr_dir / "deterministic_checks.json").write_text(
            json.dumps(checks, indent=2, ensure_ascii=False) + "\n"
        )

        if args.no_agent:
            report = deterministic_report(pr, artifacts["metadata"], checks)
            report_file.write_text(report)
            verdict = "NEEDS_FIX" if checks["actionable_count"] else "PASS"
            non_blocking = sum(
                1 for item in checks["findings"] if item["severity"] in {"medium", "low"}
            )
            end = utc_now()
            return AuditResult(
                pr=pr,
                url=artifacts["metadata"].get("url"),
                title=artifacts["metadata"].get("title"),
                status="success",
                verdict=verdict,
                highest_severity=checks["highest_severity"],
                blocking_count=checks["blocking_count"],
                non_blocking_count=non_blocking,
                start_time=start,
                end_time=end,
                duration_seconds=int(time.time() - start_epoch),
                pr_dir=str(pr_dir),
                report_file=str(report_file),
                conversation_file=None,
                log_file=None,
            )

        prompt = render_prompt(prompt_template, pr_dir, args, checkout_dir=None)
        env = os.environ.copy()
        extend_no_proxy(env)
        if args.anthropic_base_url:
            env["ANTHROPIC_BASE_URL"] = args.anthropic_base_url
        if args.anthropic_auth_token:
            env["ANTHROPIC_AUTH_TOKEN"] = args.anthropic_auth_token

        cmd = build_agent_cmd(args, prompt, pr_dir, checkout_dir=None)
        returncode, stdout, stderr = run_streaming_agent(
            cmd,
            pr_dir,
            args.timeout * 60,
            env,
            log_file,
            conversation_file,
            prompt,
        )
        report = extract_agent_report(stdout, stderr)
        if returncode != 0:
            report = f"# PR {pr} Audit\n\n## Verdict\nAGENT_FAILED\n\nAgent exited with {returncode}.\n\n```\n{report[-4000:]}\n```\n"
        report_file.write_text(report.rstrip() + "\n")

        machine = parse_machine_summary(report)
        verdict = str(machine.get("verdict") or ("AGENT_FAILED" if returncode else "PASS"))
        highest = str(machine.get("highest_severity") or checks["highest_severity"])
        blocking = int(machine.get("blocking_count") or checks["blocking_count"])
        fallback_non_blocking = sum(
            1 for item in checks["findings"] if item["severity"] in {"medium", "low"}
        )
        non_blocking = int(machine.get("non_blocking_count") or fallback_non_blocking)
        agent_actionable = non_blocking > 0 and highest in {"medium", "low"}
        if returncode == 0 and verdict == "PASS" and (blocking > 0 or checks["actionable_count"] > 0 or agent_actionable):
            verdict = "NEEDS_FIX"
        end = utc_now()
        return AuditResult(
            pr=pr,
            url=artifacts["metadata"].get("url"),
            title=artifacts["metadata"].get("title"),
            status="success" if returncode == 0 else "agent_failed",
            verdict=verdict,
            highest_severity=highest,
            blocking_count=blocking,
            non_blocking_count=non_blocking,
            start_time=start,
            end_time=end,
            duration_seconds=int(time.time() - start_epoch),
            pr_dir=str(pr_dir),
            report_file=str(report_file),
            conversation_file=str(conversation_file),
            log_file=str(log_file),
            error_summary=None if returncode == 0 else f"agent exited {returncode}",
        )
    except subprocess.TimeoutExpired as exc:
        end = utc_now()
        message = f"Timed out after {args.timeout} minutes: {exc}"
        pr_dir.mkdir(parents=True, exist_ok=True)
        report_file.write_text(f"# PR {pr} Audit\n\n## Verdict\nAGENT_FAILED\n\n{message}\n")
        return AuditResult(
            pr=pr,
            url=None,
            title=None,
            status="timeout",
            verdict="AGENT_FAILED",
            highest_severity="high",
            blocking_count=1,
            non_blocking_count=0,
            start_time=start,
            end_time=end,
            duration_seconds=int(time.time() - start_epoch),
            pr_dir=str(pr_dir),
            report_file=str(report_file),
            conversation_file=str(conversation_file),
            log_file=str(log_file),
            error_summary=message,
        )
    except Exception as exc:
        end = utc_now()
        pr_dir.mkdir(parents=True, exist_ok=True)
        report_file.write_text(f"# PR {pr} Audit\n\n## Verdict\nAGENT_FAILED\n\n{type(exc).__name__}: {exc}\n")
        return AuditResult(
            pr=pr,
            url=None,
            title=None,
            status="failed",
            verdict="AGENT_FAILED",
            highest_severity="high",
            blocking_count=1,
            non_blocking_count=0,
            start_time=start,
            end_time=end,
            duration_seconds=int(time.time() - start_epoch),
            pr_dir=str(pr_dir),
            report_file=str(report_file),
            conversation_file=str(conversation_file),
            log_file=str(log_file),
            error_summary=f"{type(exc).__name__}: {exc}",
        )


def deterministic_report(pr: int, metadata: dict, checks: dict) -> str:
    verdict = "NEEDS_FIX" if checks["actionable_count"] else "PASS"
    blocking = [item for item in checks["findings"] if item["severity"] == "high"]
    actionable = [item for item in checks["findings"] if item["severity"] in {"medium", "low"}]
    info = [item for item in checks["findings"] if item["severity"] == "info"]
    lines = [
        f"# PR {pr} Audit",
        "",
        "## Verdict",
        verdict,
        "",
        "## Blocking Findings",
    ]
    if not blocking:
        lines.append("None")
    else:
        for idx, item in enumerate(blocking, 1):
            lines.append(f"{idx}. {item['check']}: {item['message']}")
            lines.extend(f"   - {ev}" for ev in item.get("evidence", []))
    lines.extend(["", "## Actionable Non-blocking Findings"])
    if not actionable:
        lines.append("None")
    else:
        for idx, item in enumerate(actionable, 1):
            lines.append(f"{idx}. [{item['severity']}] {item['check']}: {item['message']}")
            lines.extend(f"   - {ev}" for ev in item.get("evidence", []))
    lines.extend(["", "## Info Findings"])
    if not info:
        lines.append("None")
    else:
        for idx, item in enumerate(info, 1):
            lines.append(f"{idx}. {item['check']}: {item['message']}")
            lines.extend(f"   - {ev}" for ev in item.get("evidence", []))
    lines.extend([
        "",
        "## Suggested Fix",
        "None" if not checks["actionable_count"] else "Review and repair the deterministic findings above.",
        "",
        "## Evidence Checked",
        "- deterministic checks: yes",
        "- diff.patch: no agent mode",
        "- skill rules: deterministic subset",
        "",
        "## Machine Summary",
        "```json",
        json.dumps({
            "verdict": verdict,
            "blocking_count": len(blocking),
            "non_blocking_count": len(actionable),
            "info_count": len(info),
            "highest_severity": checks["highest_severity"],
        }, ensure_ascii=False, indent=2),
        "```",
        "",
    ])
    return "\n".join(lines)


def write_summary(run_dir: Path, results: list[AuditResult]) -> None:
    payload = [asdict(item) for item in sorted(results, key=lambda item: item.pr)]
    (run_dir / "summary.json").write_text(json.dumps(payload, indent=2, ensure_ascii=False) + "\n")

    lines = [
        "# Batch PR Audit Summary",
        "",
        f"- Time: {run_dir.name}",
        f"- PRs: {len(results)}",
        "",
        "| PR | Verdict | Severity | Blocking | Non-blocking | Report |",
        "|---:|---|---|---:|---:|---|",
    ]
    for item in sorted(results, key=lambda item: item.pr):
        rel = Path(item.report_file).relative_to(run_dir)
        label = f"#{item.pr}"
        if item.url:
            label = f"[#{item.pr}]({item.url})"
        lines.append(
            f"| {label} | {item.verdict} | {item.highest_severity} | "
            f"{item.blocking_count} | {item.non_blocking_count} | [{rel}]({rel}) |"
        )
    (run_dir / "summary.md").write_text("\n".join(lines) + "\n")


