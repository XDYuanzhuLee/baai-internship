def utc_now() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def run_cmd(cmd: list[str], cwd: Path, timeout: int | None = None) -> subprocess.CompletedProcess[str]:
    return subprocess.run(cmd, cwd=str(cwd), text=True, capture_output=True, timeout=timeout)


def extend_no_proxy(env: dict[str, str]) -> None:
    existing: list[str] = []
    for key in ("NO_PROXY", "no_proxy"):
        existing.extend(part.strip() for part in env.get(key, "").split(",") if part.strip())
    merged: list[str] = []
    for host in [*existing, *LOCAL_NO_PROXY_HOSTS]:
        if host not in merged:
            merged.append(host)
    value = ",".join(merged)
    env["NO_PROXY"] = value
    env["no_proxy"] = value


def load_env_file(path: Path) -> dict[str, str]:
    values: dict[str, str] = {}
    if not path.exists():
        return values
    for raw in path.read_text().splitlines():
        line = raw.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        values[key.strip()] = os.path.expandvars(value.strip().strip('"').strip("'"))
    return values


def safe_name(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.+-]+", "_", value).strip("_") or "item"


def parse_pr_number(raw: str) -> int:
    match = PR_NUMBER_RE.search(raw.strip())
    if not match:
        raise ValueError(f"Could not parse PR number from: {raw}")
    return int(match.group(1))


def load_pr_numbers(args: argparse.Namespace) -> list[int]:
    numbers: list[int] = []
    if args.prs:
        for part in re.split(r"[,\s]+", args.prs):
            if part.strip():
                numbers.append(parse_pr_number(part))
    if args.prs_file:
        for raw in args.prs_file.read_text().splitlines():
            line = raw.strip()
            if line and not line.startswith("#"):
                numbers.append(parse_pr_number(line))
    if args.query:
        cmd = [
            "gh",
            "pr",
            "list",
            "--repo",
            args.repo,
            "--search",
            args.query,
            "--json",
            "number",
            "--limit",
            str(args.query_limit),
        ]
        result = run_cmd(cmd, Path.cwd(), timeout=60)
        if result.returncode != 0:
            raise RuntimeError(f"gh pr list failed: {result.stderr or result.stdout}")
        for item in json.loads(result.stdout):
            numbers.append(int(item["number"]))

    deduped: list[int] = []
    seen: set[int] = set()
    for number in numbers:
        if number not in seen:
            seen.add(number)
            deduped.append(number)
    return deduped


def collect_pr_artifacts(pr: int, args: argparse.Namespace, pr_dir: Path) -> dict:
    pr_dir.mkdir(parents=True, exist_ok=True)

    view_cmd = [
        "gh",
        "pr",
        "view",
        str(pr),
        "--repo",
        args.repo,
        "--json",
        "number,title,url,author,headRefName,baseRefName,body,state,isDraft,mergeable",
    ]
    view = run_cmd(view_cmd, Path.cwd(), timeout=60)
    if view.returncode != 0:
        raise RuntimeError(f"gh pr view {pr} failed: {view.stderr or view.stdout}")
    pr_json = json.loads(view.stdout)
    (pr_dir / "pr.json").write_text(json.dumps(pr_json, indent=2, ensure_ascii=False) + "\n")

    diff = run_cmd(["gh", "pr", "diff", str(pr), "--repo", args.repo], Path.cwd(), timeout=120)
    if diff.returncode != 0:
        raise RuntimeError(f"gh pr diff {pr} failed: {diff.stderr or diff.stdout}")
    (pr_dir / "diff.patch").write_text(diff.stdout)

    files = run_cmd(["gh", "pr", "diff", str(pr), "--repo", args.repo, "--name-only"], Path.cwd(), timeout=120)
    if files.returncode != 0:
        names = parse_files_from_diff(diff.stdout)
    else:
        names = [line.strip() for line in files.stdout.splitlines() if line.strip()]
    (pr_dir / "files.txt").write_text("\n".join(names) + ("\n" if names else ""))

    return {"metadata": pr_json, "diff": diff.stdout, "files": names}


def parse_files_from_diff(diff_text: str) -> list[str]:
    files: list[str] = []
    for line in diff_text.splitlines():
        if line.startswith("diff --git "):
            parts = line.split()
            if len(parts) >= 4:
                path = parts[3]
                if path.startswith("b/"):
                    path = path[2:]
                files.append(path)
    return files


def parse_added_lines_by_file(diff_text: str) -> dict[str, list[tuple[int | None, str]]]:
    """Return added lines grouped by destination file.

    The line number is the new-file line number when the hunk header provides
    enough information. It is best-effort and intended for report evidence only.
    """
    by_file: dict[str, list[tuple[int | None, str]]] = {}
    current: str | None = None
    new_line: int | None = None
    for raw in diff_text.splitlines():
        if raw.startswith("diff --git "):
            current = None
            new_line = None
            continue
        if raw.startswith("+++ b/"):
            current = raw[len("+++ b/") :]
            by_file.setdefault(current, [])
            continue
        if raw.startswith("@@"):
            match = re.search(r"\+(\d+)(?:,(\d+))?", raw)
            new_line = int(match.group(1)) if match else None
            continue
        if current is None:
            continue
        if raw.startswith("+") and not raw.startswith("+++"):
            by_file[current].append((new_line, raw[1:]))
            if new_line is not None:
                new_line += 1
        elif raw.startswith("-") and not raw.startswith("---"):
            continue
        elif new_line is not None:
            new_line += 1
    return by_file


def infer_operator_ids(files: list[str], diff_text: str, title: str | None) -> set[str]:
    ids: set[str] = set()
    for path in files:
        match = re.match(r"src/flag_gems/ops/(.+)\.py$", path)
        if match and not match.group(1).startswith("__"):
            ids.add(match.group(1).lstrip("_"))
        match = re.match(r"tests/test_(.+)\.py$", path)
        if match:
            ids.add(match.group(1).lstrip("_"))
        match = re.match(r"benchmark/test_(.+)\.py$", path)
        if match:
            ids.add(match.group(1).lstrip("_"))
        match = BACKEND_OP_PATH_RE.match(path)
        if match and match.group(2) != "__init__":
            ids.add(match.group(2).lstrip("_"))

    for line in diff_text.splitlines():
        match = re.match(r"\+\s+- id:\s+(\S+)", line)
        if match:
            ids.add(match.group(1).lstrip("_"))

    if title:
        title_match = re.search(r"Add\s+([A-Za-z0-9_.]+)\s+operator", title)
        if title_match:
            ids.add(title_match.group(1).lstrip("_"))
    return ids


def base_id(value: str) -> str:
    value = value.strip().strip('"').strip("'").lstrip("_")
    for suffix in ("_out", "_scalar", "_tensor", "_dtype", "_dtype_out"):
        if value.endswith(suffix):
            return value[: -len(suffix)]
    if value.endswith("_") and len(value) > 1:
        return value[:-1]
    return value.split(".")[0]


def belongs_to_current(value: str, current_ids: set[str]) -> bool:
    if not current_ids:
        return False
    normalized = canonical_variant_id(value)
    candidates = {normalized, base_id(normalized)}
    return bool(candidates & current_ids)


def canonical_variant_id(value: str) -> str:
    value = value.strip().strip('"').strip("'").lstrip("_")
    if value.startswith("special."):
        value = value.replace("special.", "special_", 1)
    value = value.replace(".out", "_out")
    return value.replace(".", "_")


def backend_scope_state(files: list[str], current_ids: set[str]) -> dict[str, set[str]]:
    state: dict[str, set[str]] = {}
    for path in files:
        match = BACKEND_OP_PATH_RE.match(path)
        if not match or match.group(2) == "__init__":
            continue
        backend = match.group(1)
        op_name = match.group(2)
        if belongs_to_current(op_name, current_ids):
            state.setdefault(backend, set()).add(op_name)
    return state


def infer_fused_ids(files: list[str], diff_text: str, current_ids: set[str]) -> set[str]:
    fused_ids: set[str] = set()
    for path in files:
        match = FUSED_OP_PATH_RE.match(path)
        if match and match.group(1) != "__init__":
            fused_ids.add(canonical_variant_id(match.group(1)))
        match = BACKEND_FUSED_OP_PATH_RE.match(path)
        if match and match.group(2) != "__init__":
            fused_ids.add(canonical_variant_id(match.group(2)))

    for line in diff_text.splitlines():
        match = re.match(r"\+\s+- id:\s+(\S+)", line)
        if match:
            op_id = canonical_variant_id(match.group(1))
            if "fused" in op_id:
                fused_ids.add(op_id)

    for op_id in current_ids:
        if "fused" in canonical_variant_id(op_id):
            fused_ids.add(canonical_variant_id(op_id))
    return fused_ids


def is_allowed_operator_scoped_file(path: str, current_ids: set[str], backend_state: dict[str, set[str]]) -> bool:
    if path in {
        "conf/operators.yaml",
        "src/flag_gems/__init__.py",
        "src/flag_gems/ops/__init__.py",
        "src/flag_gems/fused/__init__.py",
        "benchmark/core_shapes.yaml",
    }:
        return True
    if path.startswith("src/flag_gems/runtime/backend/_nvidia/") and path.endswith("tune_configs.yaml"):
        return True
    patterns = [
        r"src/flag_gems/ops/(.+)\.py$",
        r"tests/test_(.+)\.py$",
        r"benchmark/test_(.+)\.py$",
    ]
    for pattern in patterns:
        match = re.match(pattern, path)
        if match:
            return belongs_to_current(match.group(1), current_ids)
    fused_op = FUSED_OP_PATH_RE.match(path)
    if fused_op:
        return belongs_to_current(fused_op.group(1), current_ids)
    backend_init = BACKEND_OPS_INIT_RE.match(path)
    if backend_init:
        return bool(backend_state.get(backend_init.group(1)))
    backend_tune = BACKEND_TUNE_CONFIG_RE.match(path)
    if backend_tune:
        return bool(backend_state.get(backend_tune.group(1)))
    backend_fused_init = BACKEND_FUSED_INIT_RE.match(path)
    if backend_fused_init:
        return True
    backend_fused_op = BACKEND_FUSED_OP_PATH_RE.match(path)
    if backend_fused_op:
        return belongs_to_current(backend_fused_op.group(2), current_ids)
    backend_op = BACKEND_OP_PATH_RE.match(path)
    if backend_op:
        return belongs_to_current(backend_op.group(2), current_ids)
    return False


def evidence_line(path: str, line_no: int | None, text: str) -> str:
    prefix = f"{path}:{line_no}" if line_no is not None else path
    return f"{prefix}: {text.strip()[:220]}"


def added_text_for(path: str, added_by_file: dict[str, list[tuple[int | None, str]]]) -> str:
    return "\n".join(text for _, text in added_by_file.get(path, []))


def is_added_new_file(path: str, added_by_file: dict[str, list[tuple[int | None, str]]]) -> bool:
    line_numbers = [line_no for line_no, _ in added_by_file.get(path, []) if line_no is not None]
    return bool(line_numbers) and min(line_numbers) == 1


def added_line_evidence(path: str, added_by_file: dict[str, list[tuple[int | None, str]]], pattern: str) -> list[str]:
    hits: list[str] = []
    regex = re.compile(pattern)
    for line_no, text in added_by_file.get(path, []):
        if regex.search(text):
            hits.append(evidence_line(path, line_no, text))
    return hits


def deterministic_checks(metadata: dict, files: list[str], diff_text: str) -> dict:
    title = metadata.get("title")
    body = metadata.get("body") or ""
    current_ids = infer_operator_ids(files, diff_text, title)
    added_by_file = parse_added_lines_by_file(diff_text)
    backend_state = backend_scope_state(files, current_ids)
    fused_ids = infer_fused_ids(files, diff_text, current_ids)
    findings: list[Finding] = []

    def add(severity: str, check: str, message: str, evidence: list[str]) -> None:
        findings.append(Finding(severity, check, message, evidence[:10]))

    infra = [path for path in files if path in INFRA_FILES or path.startswith(INFRA_PREFIXES)]
    if infra:
        add("high", "infra_files", "PR touches global infra/dependency files.", infra)

    unexpected_files = [
        path for path in files
        if not is_allowed_operator_scoped_file(path, current_ids, backend_state)
    ]
    if unexpected_files:
        add(
            "medium",
            "unexpected_files",
            "PR touches files outside the normal single-operator scope.",
            unexpected_files,
        )

    op_files = [path for path in files if re.match(r"src/flag_gems/ops/(?!__init__\.py).+\.py$", path)]
    op_roots = {Path(path).stem.lstrip("_") for path in op_files}
    if len(op_roots) > 1:
        add("high", "multiple_operator_files", "PR contains multiple operator implementation files.", sorted(op_files))

    deleted_yaml_ids: list[str] = []
    added_yaml_ids: list[str] = []
    deleted_config: list[str] = []
    added_config: list[str] = []
    deleted_exports: list[str] = []
    bad_special_for: list[str] = []
    bad_special_config: list[str] = []
    special_logger_mismatches: list[str] = []
    added_pytest_marks: set[str] = set()
    added_benchmark_op_names: set[str] = set()
    added_out_functions: set[str] = set()
    added_inplace_functions: set[str] = set()
    manual_pr_commands: list[str] = []

    for line in diff_text.splitlines():
        if line.startswith("---") or line.startswith("+++"):
            continue
        stripped = line[1:].strip() if line[:1] in {"+", "-"} else line.strip()
        if line.startswith("-"):
            m = re.match(r"-\s+- id:\s+(\S+)", line)
            if m:
                deleted_yaml_ids.append(m.group(1))
            m = re.match(r'-\s+\("([^"]+)",\s*(\w+)', line)
            if m:
                deleted_config.append(f'{m.group(1)} -> {m.group(2)}')
            m = re.match(r'-\s+"([^"]+)",\s*$', line)
            if m:
                deleted_exports.append(m.group(1))
        elif line.startswith("+"):
            m = re.match(r"\+\s+- id:\s+(\S+)", line)
            if m:
                added_yaml_ids.append(m.group(1))
            m = re.match(r'\+\s+\("([^"]+)",\s*(\w+)', line)
            if m:
                added_config.append(f'{m.group(1)} -> {m.group(2)}')
                if m.group(1).startswith("special_"):
                    bad_special_config.append(m.group(1))
            m = re.match(r"\+\s+-\s+(special_[A-Za-z0-9_.]+)\s*$", line)
            if m:
                bad_special_for.append(m.group(1))
            m = re.search(r'logger\.debug\(\s*["\']GEMS\s+([A-Z0-9_]+)', line)
            if m:
                logged = m.group(1)
                for current_id in current_ids:
                    if current_id.startswith("special_"):
                        expected = current_id.upper()
                        if current_id.endswith("_out"):
                            expected = current_id.upper()
                        if logged == expected or logged.startswith(expected + " "):
                            break
                        if logged.endswith(expected.removeprefix("SPECIAL_")):
                            special_logger_mismatches.append(
                                f"{logged} should include full special operator prefix, e.g. {expected}"
                            )
                            break
            m = re.search(r"@pytest\.mark\.([A-Za-z0-9_]+)", line)
            if m:
                added_pytest_marks.add(m.group(1))
            m = re.search(r"\bop_name\s*=\s*['\"]([^'\"]+)['\"]", line)
            if m:
                added_benchmark_op_names.add(m.group(1))
        if MANUAL_PR_RE.search(stripped):
            manual_pr_commands.append(stripped)

    unrelated_deleted_ids = [
        item for item in deleted_yaml_ids if not belongs_to_current(item, current_ids)
    ]
    if unrelated_deleted_ids:
        add(
            "high",
            "unrelated_yaml_id_deleted",
            "PR deletes YAML operator ids that do not appear to belong to the current operator.",
            unrelated_deleted_ids,
        )

    unrelated_deleted_config = [
        item for item in deleted_config if not belongs_to_current(item.split(" -> ", 1)[0], current_ids)
    ]
    if unrelated_deleted_config:
        add(
            "high",
            "unrelated_full_config_deleted",
            "PR deletes _FULL_CONFIG entries that do not appear to belong to the current operator.",
            unrelated_deleted_config,
        )

    unrelated_deleted_exports = [
        item for item in deleted_exports if not belongs_to_current(item, current_ids)
    ]
    if unrelated_deleted_exports:
        add(
            "medium",
            "unrelated_export_deleted",
            "PR deletes __all__ exports that do not appear to belong to the current operator.",
            unrelated_deleted_exports,
        )

    if bad_special_for:
        add(
            "high",
            "bad_special_yaml_for",
            "New special_* YAML for entries should use special.xxx dispatch format.",
            [f"{item} -> {item.replace('special_', 'special.', 1)}" for item in bad_special_for],
        )

    if bad_special_config:
        add(
            "high",
            "bad_special_full_config",
            "New special_* _FULL_CONFIG keys should use special.xxx dispatch format.",
            [f"{item} -> {item.replace('special_', 'special.', 1)}" for item in bad_special_config],
        )

    if special_logger_mismatches:
        add(
            "low",
            "special_logger_prefix",
            "Logger messages for special_* operators should keep the full operator name.",
            sorted(set(special_logger_mismatches)),
        )

    if manual_pr_commands:
        add("high", "manual_pr_command", "Diff contains manual gh pr commands.", manual_pr_commands)

    duplicate_added_yaml_ids = sorted({item for item in added_yaml_ids if added_yaml_ids.count(item) > 1})
    if duplicate_added_yaml_ids:
        add("high", "yaml_duplicate_added_id", "PR adds duplicate operators.yaml ids.", duplicate_added_yaml_ids)

    added_public_functions = extract_public_operator_functions(added_by_file, current_ids)
    added_out_functions, added_inplace_functions = extract_operator_variant_functions(added_by_file)

    body_findings = check_pr_body(body)
    for severity, check, message, evidence in body_findings:
        add(severity, check, message, evidence)

    added_line_findings = check_added_lines(added_by_file)
    for severity, check, message, evidence in added_line_findings:
        add(severity, check, message, evidence)

    device_findings = check_device_hardcode(added_by_file)
    for severity, check, message, evidence in device_findings:
        add(severity, check, message, evidence)

    structure_findings = check_new_file_structure(added_by_file, current_ids)
    for severity, check, message, evidence in structure_findings:
        add(severity, check, message, evidence)

    backend_findings = check_backend_specialization_files(
        files=files,
        added_by_file=added_by_file,
        current_ids=current_ids,
        backend_state=backend_state,
    )
    for severity, check, message, evidence in backend_findings:
        add(severity, check, message, evidence)

    fused_findings = check_fused_layout(
        files=files,
        added_by_file=added_by_file,
        current_ids=current_ids,
        fused_ids=fused_ids,
    )
    for severity, check, message, evidence in fused_findings:
        add(severity, check, message, evidence)

    yaml_block_findings = check_yaml_added_blocks(added_by_file, current_ids)
    for severity, check, message, evidence in yaml_block_findings:
        add(severity, check, message, evidence)

    registration_findings = check_registration_surfaces(
        added_by_file=added_by_file,
        added_public_functions=added_public_functions,
        added_full_config=added_config,
    )
    for severity, check, message, evidence in registration_findings:
        add(severity, check, message, evidence)

    kernel_risk_findings = check_kernel_risks(added_by_file, current_ids)
    for severity, check, message, evidence in kernel_risk_findings:
        add(severity, check, message, evidence)

    logger_findings = check_logger_usage(added_by_file, added_public_functions)
    for severity, check, message, evidence in logger_findings:
        add(severity, check, message, evidence)

    variant_findings = check_variant_consistency(
        added_out_functions=added_out_functions,
        added_inplace_functions=added_inplace_functions,
        added_yaml_ids=added_yaml_ids,
        added_full_config=added_config,
        added_pytest_marks=added_pytest_marks,
        added_benchmark_op_names=added_benchmark_op_names,
    )
    for severity, check, message, evidence in variant_findings:
        add(severity, check, message, evidence)

    coverage_findings = check_public_function_coverage(
        added_public_functions=added_public_functions,
        added_yaml_ids=added_yaml_ids,
        added_pytest_marks=added_pytest_marks,
        added_benchmark_op_names=added_benchmark_op_names,
        current_ids=current_ids,
    )
    for severity, check, message, evidence in coverage_findings:
        add(severity, check, message, evidence)

    benchmark_fairness_findings = check_benchmark_fairness(added_by_file, current_ids, fused_ids)
    for severity, check, message, evidence in benchmark_fairness_findings:
        add(severity, check, message, evidence)

    specialization_dependency_findings = check_specialization_pr_dependency(
        metadata=metadata,
        files=files,
        current_ids=current_ids,
        backend_state=backend_state,
        repo="flagos-ai/FlagGems",
    )
    for severity, check, message, evidence in specialization_dependency_findings:
        add(severity, check, message, evidence)

    speedup_findings = extract_speedup_findings(body, current_ids)
    for item in speedup_findings:
        add(
            "info",
            "low_speedup",
            "PR body reports little or no arithmetic mean speedup.",
            [item],
        )

    severity_order = {"high": 4, "medium": 3, "low": 2, "info": 1}
    highest = "none"
    for finding in findings:
        if severity_order.get(finding.severity, 0) > severity_order.get(highest, 0):
            highest = finding.severity

    return {
        "current_operator_ids_inferred": sorted(current_ids),
        "backend_specializations_inferred": {backend: sorted(ids) for backend, ids in sorted(backend_state.items())},
        "fused_operator_ids_inferred": sorted(fused_ids),
        "changed_files": files,
        "added_yaml_ids": added_yaml_ids,
        "deleted_yaml_ids": deleted_yaml_ids,
        "added_full_config": added_config,
        "deleted_full_config": deleted_config,
        "added_public_operator_functions": sorted(added_public_functions),
        "findings": [asdict(item) for item in findings],
        "highest_severity": highest,
        "blocking_count": sum(1 for item in findings if item.severity == "high"),
        "actionable_count": sum(1 for item in findings if item.severity in {"high", "medium", "low"}),
    }


