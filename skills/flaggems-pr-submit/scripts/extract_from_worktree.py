class Colors:
    OK = "\033[92m"
    FAIL = "\033[91m"
    BOLD = "\033[1m"
    CYAN = "\033[96m"
    END = "\033[0m"


def ok(msg):
    print(f"  {Colors.OK}✓{Colors.END} {msg}")


def warn(msg):
    print(f"  {Colors.CYAN}⚠{Colors.END} {msg}")


def fatal(msg):
    print(f"  {Colors.FAIL}✗ FATAL: {msg}{Colors.END}")
    sys.exit(1)


def section(msg):
    print(f"\n{Colors.BOLD}[{msg}]{Colors.END}")


def get_op_id(op_name):
    return op_name.lstrip("_")


def normalize_kernel_header(content):
    if content.startswith(REQUIRED_KERNEL_HEADER):
        return content

    if content.startswith(COPYRIGHT_LICENSE_HEADER):
        rest = content[len(COPYRIGHT_LICENSE_HEADER):]
        if rest.startswith(KERNELGEN_HEADER):
            return content
        if rest.startswith("# Generated by KernelGen"):
            rest = rest.split("\n", 1)[1] if "\n" in rest else ""
        return REQUIRED_KERNEL_HEADER + rest.lstrip("\n")

    if content.startswith("# Generated by KernelGen"):
        content = content.split("\n", 1)[1] if "\n" in content else ""

    return REQUIRED_KERNEL_HEADER + content


