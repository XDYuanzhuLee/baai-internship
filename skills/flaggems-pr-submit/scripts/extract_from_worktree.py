def _replace_tokens(content, plan):
    replacements = []
    for old in [
        plan.source_name,
        plan.impl_name,
        plan.source_op_id,
        plan.impl_op_id,
    ]:
        if old and old != plan.canonical_name and old != plan.canonical_op_id:
            replacements.append((old, plan.canonical_name))

    result = content
    # Longer tokens first so Cross_Attention is replaced before cross_attention-like substrings.
    for old, new in sorted(set(replacements), key=lambda item: len(item[0]), reverse=True):
        result = result.replace(old, new)
    return result


def canonicalize_extracted_files(plan, repo, dry_run):
    """Rename generated source-name files and contents to the canonical name."""
    if not plan.renamed:
        return

    section("Step 7: 规范名重命名")
    ok(f"source_name: {plan.source_name}")
    ok(f"canonical_name: {plan.canonical_name}")

    source_files = [
        f"src/flag_gems/ops/{plan.source_name}.py",
        f"tests/test_{plan.impl_op_id}.py",
        f"benchmark/test_{plan.impl_op_id}.py",
        "src/flag_gems/ops/__init__.py",
        "src/flag_gems/__init__.py",
        "conf/operators.yaml",
    ]

    for relpath in source_files:
        full = os.path.join(repo, relpath)
        if not os.path.isfile(full):
            continue
        with open(full) as f:
            content = f.read()
        new_content = _replace_tokens(content, plan)
        if new_content != content:
            if dry_run:
                ok(f"[DRY] 将规范化内容: {relpath}")
            else:
                with open(full, "w") as f:
                    f.write(new_content)
                ok(f"已规范化内容: {relpath}")

    renames = [
        (
            f"src/flag_gems/ops/{plan.source_name}.py",
            f"src/flag_gems/ops/{plan.canonical_name}.py",
        ),
        (
            f"tests/test_{plan.impl_op_id}.py",
            f"tests/test_{plan.canonical_op_id}.py",
        ),
        (
            f"benchmark/test_{plan.impl_op_id}.py",
            f"benchmark/test_{plan.canonical_op_id}.py",
        ),
    ]

    for old_rel, new_rel in renames:
        old = os.path.join(repo, old_rel)
        new = os.path.join(repo, new_rel)
        if old == new or not os.path.isfile(old):
            continue
        if os.path.exists(new):
            fatal(f"规范名目标文件已存在，不能覆盖: {new_rel}")
        if dry_run:
            ok(f"[DRY] 将重命名: {old_rel} → {new_rel}")
        else:
            os.rename(old, new)
            ok(f"已重命名: {old_rel} → {new_rel}")


